// Printing a simple message "Ghost in the shell"

// The #include <iostream> statement includes the Input/Output Stream library
// This allows us to use std::cin for input and std::cout for output
#include <iostream>


int main() // int main() is the entry point of the program where execution starts
{
    std::cout << "Ghost in the shell" << std::endl; // Print "Ghost in the shell" to the console
    return 0;  // return 0; indicates that the program ended successfully without any errors.
}